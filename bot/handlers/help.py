from aiogram import Router, types
from aiogram.filters import Command

router = Router()


@router.message(Command("help"))
async def cmd_help(message: types.Message):
    await message.answer(
        "📖 <b>Как пользоваться ботом</b>\n\n"
        "Бот помогает следить за средним баллом и понимать, сколько ещё оценок нужно получить.\n\n"
        "📚 <b>/subjects — Предметы</b>\n"
        "Добавляй свои предметы, переименовывай их или удаляй те, что больше не нужны.\n\n"
        "📊 <b>/grades — Оценки</b>\n"
        "Добавляй полученные оценки. При добавлении ты сразу увидишь, как изменится средний балл и сколько ещё пятёрок или четвёрок нужно до цели.\n\n"
        "🎯 <b>/summary — Сводка и прогноз</b>\n"
        "Показывает общий список всех предметов с их средним баллом и подсчитывает, сколько оценок осталось до желаемого результата (4 или 5).\n\n"
        "⚙️ <b>/settings — Настройки периодов</b>\n"
        "Выбери, как ты учишься (по четвертям или триместрам), и укажи текущий активный период.",
        parse_mode="HTML",
    )
